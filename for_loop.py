# Basic for loop

string = 'Python'
for i in string:
    print(i)
print('I have reached the end!')

languages = ['Python', 'C', 'JavaScript', 'Ruby']
for x in languages:
    print(x)
print('I have reached the end!')

# For in range loop
# for x in range(start, stop, increment):

for x in range(11):
    print(x)

for x in range(10, 11, 1):
    print(x)
